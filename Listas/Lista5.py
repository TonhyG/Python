Listaletras = ["A", "E", "D", "F", "B", "C"]

print("Lista original:", Listaletras)
Listaletras.sort()  # Ordena a lista em ordem crescente
print("Lista ordenada:", Listaletras)

Listaletras.sort(reverse=True)  # Ordena a lista em ordem decrescente
print("Lista ordenada reversa:", Listaletras)

print("\n")

