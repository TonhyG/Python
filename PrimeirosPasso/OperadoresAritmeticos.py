"""
+ Adição
- Subtração
* Multiplicação
/ Divisão
% Módulo (resto da divisão)
** Exponenciação
// Divisão inteira (quociente)
"""

numero1 = 10
numero2 = 5
print(numero1 + numero2)
print(numero1 - numero2)
print(numero1 * numero2)
print(numero1 / numero2)

print("\n\n")

numero1 = 20; numero2 = 3  # Várias variáveis podem ser declaradas na mesma linha
print(numero1 % numero2)
print(numero1 ** numero2)
print(numero1 // numero2)  # Divisão inteira, resultado sem casas decimais

print("\n\n")

print(numero1 * "a")  # Multiplicação de string, repete a string 'a' 20 vezes