# Comentario em Python, não mostra na tela

"""No python, aspas triplas são usadas para criar comentarios de múltiplas linhas"""

nome = "Anthony"

print(nome)  # Função print exibe o valor na tela"

nome = "Larissa"  # Variável pode ser alterada
print(nome)  # Exibe o novo valor da variável

idade = 20  # Variável inteira
print(idade)  # Exibe o valor da variável idade

nome = "Anthony"; sobrenome = "Guilherme"  # Várias variáveis podem ser declaradas na mesma linha
print(nome, sobrenome)  # Exibe o valor das variáveis nome e sobrenome
print(nome + sobrenome)  # Concatena e exibe o valor das variáveis nome e sobrenome