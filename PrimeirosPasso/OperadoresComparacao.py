"""
== igual : compara se os valores são iguais
!= diferente : compara se os valores são diferentes
> maior que : compara se o valor da esquerda é maior que o da direita
< menor que : compara se o valor da esquerda é menor que o da direita
>= maior ou igual : compara se o valor da esquerda é maior ou igual ao da direita
<= menor ou igual : compara se o valor da esquerda é menor ou igual ao da direita
"""

numero1 = 10
numero2 = 5

if numero1 == numero2:
    print("Os números são iguais")

if numero1 != numero2:
    print("Os números são diferentes")

if numero1 > numero2:
    print("O número 1 é maior que o número 2")

if numero1 < numero2:
    print("O número 1 é menor que o número 2")

if numero1 >= numero2:
    print("O número 1 é maior ou igual ao número 2")

if numero1 <= numero2:
    print("O número 1 é menor ou igual ao número 2")

print("\n\n")